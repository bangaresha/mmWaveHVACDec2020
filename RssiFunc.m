    reflecjEF = (1000*...
        exp(-1i*2*pi*freq*first2SecondPointjDist1/(1000*lightVel)))/first2SecondPointjDist1 +...
    (1000*exp(-1i*2*pi*freq*first2SecondPointjDist2/(1000*lightVel)))/first2SecondPointjDist2;
