    +(10*log10(tempReflecCoeff1))+(10*log10(tempReflecCoeff2))+...
        (TxAntennaGainAE(round(depBeamAngle.AziIndex),round(depBeamAngle.ZenIndex)))+ ... 
        (RxAntennaGainAE(round(arrBeamAngle.AziIndex),round(arrBeamAngle.ZenIndex)))...
        -10*log10(gammaD)-10*log10(gammaY))/10) ...
         .*complex(cos(2*pi*freq* Rx2TxRefl./lightVel + pi),sin(2*pi*freq*Rx2TxRefl./lightVel+pi));
     
     reflecjEF = (1000*...
        exp(-1i*2*pi*freq*first2SecondPointjDist1/(1000*lightVel)))/first2SecondPointjDist1 +...
    (1000*...
        exp(-1i*2*pi*freq*first2SecondPointjDist2/(1000*lightVel)))/first2SecondPointjDist2;
end

